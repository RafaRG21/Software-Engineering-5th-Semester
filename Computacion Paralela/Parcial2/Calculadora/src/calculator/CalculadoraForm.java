    private void btnResultadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResultadoActionPerformed
        segundoNumero = Float.parseFloat(this.lblCasilla.getText());
        switch(operador){
            case '+':
                this.lblCasilla.setText(sinCero(suma(primerNumero,segundoNumero)));
            break;
            case '-':
                this.lblCasilla.setText(sinCero(resta(primerNumero,segundoNumero)));
            break;
            case '*':
                this.lblCasilla.setText(sinCero(multiplicacion(primerNumero,segundoNumero)));
            break;
            case '/':
                this.lblCasilla.setText(sinCero(division(primerNumero,segundoNumero)));
            break;
            case '%':
                this.lblCasilla.setText(sinCero(modulo(primerNumero,segundoNumero)));
                break;
            case '^':
                this.lblCasilla.setText(sinCero(potencia(primerNumero,segundoNumero)));
                break;

        }//switch
    }//GEN-LAST:event_btnResultadoActionPerformed
    public String sinCero(float res){
        String retorno="";
        retorno  = String.valueOf(res);
        if(res%1==0){
            retorno = retorno.substring(0,retorno.length()-2);
        }
        return retorno;
    }
